# Clean 'Size' column
def clean_size(value):
    # Convert the value to string, handle NaN values
    if pd.isna(value):
        return np.nan
    value_str = str(value)

    if value_str == 'Varies with device':
        return np.nan
    if 'M' in value_str:
        return float(value_str.replace('M', '')) * 1024  # Convert MB to KB
    if 'k' in value_str:
        return float(value_str.replace('k', ''))  # Already in KB
    # Handle cases where the value might be a number without 'M' or 'k' or other unexpected formats
    try:
        return float(value_str) # If it's already a number (like a float from previous conversion)
    except ValueError:
        return np.nan # For any other unexpected format

df['Size'] = df['Size'].apply(clean_size)
print(df.info())

# Clean 'Installs' column
df['Installs'] = df['Installs'].str.replace('[+,]', '', regex=True).astype(float)

# Clean 'Price' column
df['Price'] = df['Price'].str.replace('$', '', regex=False).astype(float)

