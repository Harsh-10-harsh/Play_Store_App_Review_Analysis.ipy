# Define a function that calculates mean and median of numerical columns for a given 'Category'
def get_category_stats(category_code, dataframe):
    # Filter the DataFrame for the given category
    filtered_df = dataframe[dataframe['Category'] == category_code]

    # Select only numeric columns
    numeric_data = filtered_df.select_dtypes(include='number')

    # Calculate mean and median
    mean_values = numeric_data.mean()
    median_values = numeric_data.median()

    return pd.DataFrame({
        'Mean': mean_values,
        'Median': median_values
    })

